enum BSP_LATTE_REVS {
    BSP_LATTE_REV_A1X = 0x21,
    BSP_LATTE_REV_A2X = 0x22,
    BSP_LATTE_REV_A3X = 0x23,
    BSP_LATTE_REV_A4X = 0x24,
    BSP_LATTE_REV_A5X = 0x25,
    BSP_LATTE_REV_B1X = 0x26,
};

#define BSP_LATTE_REV(hwver) ((hwver & 0xFF000000) >> 24)

//BSP_HARDWARE_VERSION_LATTE_*
#define BSP_IS_LATTE(hwver) ((hwver & 0x0F000000) != 0)

//BSP_HARDWARE_VERSION_LATTE_*_EV_Y
#define BSP_IS_EV_Y(hwver) ((hwver & 0xFFFF) == 0x0011)

//BSP_HARDWARE_VERSION_BOLLYWOOD
//BSP_HARDWARE_VERSION_BOLLYWOOD_PROD_FOR_WII
#define BSP_IS_BOLLYWOOD(hwver) ((hwver >> 24) == 0x20)

//BSP_HARDWARE_VERSION_HOLLYWOOD_ENG_SAMPLE_2
//BSP_HARDWARE_VERSION_HOLLYWOOD_PROD_FOR_WII
//BSP_HARDWARE_VERSION_HOLLYWOOD_CORTADO
//BSP_HARDWARE_VERSION_HOLLYWOOD_CORTADO_ESPRESSO
#define BSP_IS_HOLLYWOOD(hwver) ((hwver >> 24) == 0x10)

//BSP_HARDWARE_VERSION_HOLLYWOOD_ENG_SAMPLE_1
#define BSP_IS_HOLLYWOOD_ES1(hwver) ((hwver >> 24) == 0x00)

#endif //_BSP_HWREVS_H_
